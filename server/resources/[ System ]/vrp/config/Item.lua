local List = {}

local function convertItems()
	local items = module('vrp',"config/Itemlist") or {}
    for Item,v in pairs(items) do
        List[Item] = {
            ["Index"] = v.index,
            ["Name"] = v.name,
            ["Type"] = v.type,
            ["Weight"] = v.weight,
        }
    end
end

RegisterNetEvent("Reborn:reloadInfos",function()
	convertItems()
end)
-----------------------------------------------------------------------------------------------------------------------------------------
-- ITEMBODY
-----------------------------------------------------------------------------------------------------------------------------------------
function itemBody(Item)
	local Split = splitString(Item,"-")
	local Item = Split[1]

	if List[Item] then
		return List[Item]
	end

	return false
end
