        { areaSize = 30, speedMultiplier = 0.75 },
        { areaSize = 40, speedMultiplier = 1.0 },
        { areaSize = 50, speedMultiplier = 1.25 },
        { areaSize = 60, speedMultiplier = 1.5 },
    }, BUTTONS_TO_PRESS)
end
