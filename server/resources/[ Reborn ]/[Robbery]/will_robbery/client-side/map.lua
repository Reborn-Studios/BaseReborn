setupObjects = {}
function CreateBettaMap()
    if setupObjects ~= {} then
        RemoveBettaMap()
    end

    for k, v in pairs(setupInterior) do
        loadModel(k)
        for x, y in pairs(v) do
            setupObjects[#setupObjects + 1] = CreateObject(GetHashKey(k), y['coords'].x, y['coords'].y, y['coords'].z, false, false, true)
            if y['heading'] then
                SetEntityHeading(setupObjects[#setupObjects], y['heading'])
            else
                SetEntityRotation(setupObjects[#setupObjects], y['rotation'].x, y['rotation'].y, y['rotation'].z, 0, false)
            end
            FreezeEntityPosition(setupObjects[#setupObjects], true)
            Wait(10)
        end
        SetModelAsNoLongerNeeded(GetHashKey(k))
    end
end

function RemoveBettaMap()
    for k, v in pairs(setupObjects) do
        DeleteObject(v)
    end

    setupObjects = {}
end

AddEventHandler('onResourceStop', function(p1)
    if p1 == GetCurrentResourceName() then
        RemoveBettaMap()
    end
end)