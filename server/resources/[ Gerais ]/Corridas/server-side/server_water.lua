-----------------------------------------------------------------------------------------------------------------------------------------
-- CONNECTION
-----------------------------------------------------------------------------------------------------------------------------------------
Water = {}
Tunnel.bindInterface("Water",Water)
-----------------------------------------------------------------------------------------------------------------------------------------
-- VARIABLES
-----------------------------------------------------------------------------------------------------------------------------------------
local race = 1
-----------------------------------------------------------------------------------------------------------------------------------------
-- PAYMENTS
-----------------------------------------------------------------------------------------------------------------------------------------
local payments = Config.waterRace['payments']
-----------------------------------------------------------------------------------------------------------------------------------------
-- RACESELECT
-----------------------------------------------------------------------------------------------------------------------------------------
function Water.raceSelect()
	return math.random(#Config.waterRace.races)
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- PAYMENTMETHOD
-----------------------------------------------------------------------------------------------------------------------------------------
function Water.paymentMethod()
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		local value = math.random(payments[race][1],payments[race][2])
		vRP.giveInventoryItem(user_id,"dollars",parseInt(value),true)
		TriggerClientEvent("vrp_sound:source",source,"coin",0.5)
		vRP.createWeebHook(Webhooks.webhookraces,"```prolog\n[ID]: "..user_id.."\n[Ganhou da corrida aquatica]: $"..value..os.date("\n[Data]: %d/%m/%Y [Hora]: %H:%M:%S").." \r```")
	end
end
