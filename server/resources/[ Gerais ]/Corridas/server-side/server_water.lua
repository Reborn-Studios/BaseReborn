-----------------------------------------------------------------------------------------------------------------------------------------
-- VRP
-----------------------------------------------------------------------------------------------------------------------------------------
local Tunnel = module("vrp","lib/Tunnel")
local Proxy = module("vrp","lib/Proxy")
vRP = Proxy.getInterface("vRP")
-----------------------------------------------------------------------------------------------------------------------------------------
-- CONNECTION
-----------------------------------------------------------------------------------------------------------------------------------------
wrVRP = {}
Tunnel.bindInterface("Water",wrVRP)
-----------------------------------------------------------------------------------------------------------------------------------------
-- VARIABLES
-----------------------------------------------------------------------------------------------------------------------------------------
local race = 1
-----------------------------------------------------------------------------------------------------------------------------------------
-- PAYMENTS
-----------------------------------------------------------------------------------------------------------------------------------------
local payments = Config.waterRace['payments']
-----------------------------------------------------------------------------------------------------------------------------------------
-- RACESELECT
-----------------------------------------------------------------------------------------------------------------------------------------
function wrVRP.raceSelect()
	return parseInt(race)
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- PAYMENTMETHOD
-----------------------------------------------------------------------------------------------------------------------------------------
function wrVRP.paymentMethod()
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		local value = math.random(payments[race][1],payments[race][2])

		vRP.giveInventoryItem(user_id,"dollars",parseInt(value),true)
		TriggerClientEvent("vrp_sound:source",source,"coin",0.5)
	end
end